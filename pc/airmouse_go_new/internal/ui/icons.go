// GenerateColoredIcon creates a colored circle icon programmatically
func GenerateColoredIcon(col color.Color, size int) ([]byte, error) {
    img := image.NewRGBA(image.Rect(0, 0, size, size))
    
    // Fill background
    for x := 0; x < size; x++ {
        for y := 0; y < size; y++ {
            img.Set(x, y, color.Transparent)
        }
    }
    
    // Draw circle
    center := size / 2
    radius := size / 2 - 1
    
    for x := 0; x < size; x++ {
        for y := 0; y < size; y++ {
            dx := x - center
            dy := y - center
            if dx*dx+dy*dy <= radius*radius {
                img.Set(x, y, col)
            }
        }
    }
    
    var buf bytes.Buffer
    if err := png.Encode(&buf, img); err != nil {
        return nil, err
    }
    
    return buf.Bytes(), nil
}